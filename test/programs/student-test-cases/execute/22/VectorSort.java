class Comp {
    public boolean eq(int a, int b) {
        return !(a < b) && !(b < a);
    }

    public boolean ge(int a, int b) {
        return !(a < b);
    }

    public boolean gt(int a, int b) {
        return this.ge(a, b) && !this.eq(a,b);
    }
}
